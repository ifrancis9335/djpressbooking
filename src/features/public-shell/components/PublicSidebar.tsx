interface PublicSidebarProps {
  siteName: string;
}

export function PublicSidebar({ siteName }: PublicSidebarProps) {
  const pathname = usePathname();

  function isActive(href: string, exact: boolean) {
    if (exact) return pathname === href;
    return pathname === href || pathname.startsWith(href + "/");
  }

  return (
    <aside className="public-shell-sidebar">
      <div className="public-shell-sidebar-brand">
        <span className="public-shell-sidebar-brand-dot" aria-hidden="true" />
        <div>
          <p className="public-shell-sidebar-brand-name">{siteName}</p>
          <p className="public-shell-sidebar-brand-sub">Public Dashboard</p>
        </div>
      </div>

      <nav aria-label="Public site navigation" className="public-shell-nav">
        <ul className="public-shell-nav-list">
          {navItems.map((item) => {
            const active = isActive(item.href, item.exact);
            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  data-route={item.href}
                  aria-current={active ? "page" : undefined}
                  className={cn("public-shell-nav-btn", active && "is-active")}
                >
                  <span className="public-shell-nav-icon" aria-hidden="true">{item.icon}</span>
                  <span className="public-shell-nav-text">{item.label}</span>
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      <div className="public-shell-sidebar-cta">
        <Link href="/booking" className="public-shell-cta-btn w-full justify-center text-center">
          Book Your Event
        </Link>
      </div>
    </aside>
  );
}
